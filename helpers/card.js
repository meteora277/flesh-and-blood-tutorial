class card {
  constructor(
    type,
    name,
    effect,
    characterClass,
    actionType,
    attack,
    defense,
    pitch,
    cost,
    color
  ) {
    this.type = type;
    this.name = name;
    this.effect = effect;
    this.characterClass = characterClass;
    this.attack = attack;
    this.defense = defense;
    this.pitch = pitch;
    this.cost = cost;
    this.color = color;
    this.actionType = actionType;
  }
}
